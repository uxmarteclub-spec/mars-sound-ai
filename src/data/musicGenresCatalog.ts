export function mergeGenreNameLists(...lists: readonly string[][]): string[] {
  const set = new Set<string>();
  for (const list of lists) {
    for (const raw of list) {
      const n = raw?.trim();
      if (n && n !== "Todos") set.add(n);
    }
  }
  return [...set].sort((a, b) => a.localeCompare(b, "pt"));
}
